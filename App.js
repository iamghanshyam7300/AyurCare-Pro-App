/* ---------------------- MAIN NAVIGATION ---------------------- */
function AppNavigator() {
  const { isAuthenticated, isLoading, user } = useAuth();

  if (isLoading) return null;

  return (
    <NavigationContainer>
      <Stack.Navigator screenOptions={{ headerShown: false }}>
        {!isAuthenticated ? (
          <Stack.Screen name="Login" component={LoginPage} />
        ) : user?.role === "PATIENT" ? (
          <>
            <Stack.Screen name="MainTabs" component={PatientTabs} />

            {/* CHAT */}
            <Stack.Screen
              name="ChatList"
              component={ChatList}
              options={{ headerShown: false }}  // ✅ FIXED
            />
            <Stack.Screen
              name="ChatScreen"
              component={ChatScreen}
              options={({ route }) => ({
                headerShown: true,
                title: route.params?.patientName,
              })}
            />

            {/* PATIENT SETTINGS */}
            <Stack.Screen
              name="PatientSettings"
              component={PatientSettings}
              options={{ headerShown: false }}
            />

            <Stack.Screen
              name="ChangePassword"
              component={ChangePassword}
              options={{ headerShown: true, title: "Change Password" }}
            />

            <Stack.Screen
              name="EditUserProfile"
              component={EditUserProfile}
              options={{ headerShown: true, title: "Edit Profile" }}
            />
          </>
        ) : (
          <>
            <Stack.Screen name="MainTabs" component={DoctorTabs} />

            {/* CHAT */}
            <Stack.Screen
              name="ChatList"
              component={ChatList}
              options={{ headerShown: false }}  // ✅ FIXED
            />
            <Stack.Screen
              name="ChatScreen"
              component={ChatScreen}
              options={({ route }) => ({
                headerShown: true,
                title: route.params?.patientName,
              })}
            />

            {/* PATIENT MGMT */}
            <Stack.Screen
              name="PatientProfile"
              component={PatientProfile}
              options={{ headerShown: true }}
            />
            <Stack.Screen
              name="EditPatient"
              component={EditPatient}
              options={{ headerShown: true }}
            />
            <Stack.Screen
              name="AddPatient"
              component={AddPatient}
              options={{ headerShown: true, title: "Add New Patient" }}
            />

            {/* DIET */}
            <Stack.Screen name="DietPlans" component={DietPlans} />
            <Stack.Screen name="Collections" component={Collections} />
            <Stack.Screen name="Recipes" component={Recipes} />
            <Stack.Screen name="DietChart" component={DietChart} />
            <Stack.Screen name="AutoGenerateDiet" component={AutoGenerateDiet} />

            <Stack.Screen
              name="AddFoodScreen"
              component={AddFoodScreen}
              options={{ headerShown: true, title: "Add New Food" }}
            />

            {/* DOCTOR SETTINGS */}
            <Stack.Screen
              name="ChangePassword"
              component={ChangePassword}
              options={{ headerShown: true, title: "Change Password" }}
            />

            <Stack.Screen
              name="EditUserProfile"
              component={EditUserProfile}
              options={{ headerShown: true, title: "Edit Profile" }}
            />
          </>
        )}
      </Stack.Navigator>
    </NavigationContainer>
  );
}

/* ---------------------- ROOT WRAPPER ---------------------- */
export default function App() {
  return (
    <AuthProvider>
      <KeyboardProvider>
        <AppNavigator />
      </KeyboardProvider>
    </AuthProvider>
  );
}
