/* ---------------------- MAIN NAVIGATOR ---------------------- */
function AppNavigator() {
  const { isAuthenticated, isLoading, user } = useAuth();

  if (isLoading) return null;

  return (
    <NavigationContainer>
      <Stack.Navigator screenOptions={{ headerShown: false }}>

        {/* ========== NOT AUTHENTICATED ROUTES ========== */}
        {!isAuthenticated ? (
          <>
            <Stack.Screen name="Login" component={LoginPage} />

            <Stack.Screen
              name="WaitingVerification"
              component={WaitingVerification}
              options={{
                headerShown: true,
                title: "Verification Pending",
                headerStyle: { backgroundColor: colors.card },
                headerTintColor: colors.foreground,
              }}
            />
          </>
        ) : user?.role === "SUPER_ADMIN" ? (
          <>
            <Stack.Screen name="AdminMain" component={AdminTabs} />
            <Stack.Screen name="ChatEntry" component={ChatList} />
            <Stack.Screen name="ChatScreen" component={ChatScreen} />
          </>
        ) : user?.role === "PATIENT" ? (
          <>
            <Stack.Screen name="MainTabs" component={PatientTabs} />

            <Stack.Screen name="DietChart" component={DietChart} />

            <Stack.Screen
              name="ChatEntry"
              component={ChatEntryForPatient}
              options={{
                headerShown: true,
                title: "Messages",
              }}
            />

            <Stack.Screen name="ChatScreen" component={ChatScreen} />
            <Stack.Screen name="PatientSettings" component={PatientSettings} />
            <Stack.Screen name="ChangePassword" component={ChangePassword} />
            <Stack.Screen name="EditUserProfile" component={EditUserProfile} />
            <Stack.Screen name="AutoGenerateDiet" component={AutoGenerateDiet} />
          </>
        ) : (
          <>
            <Stack.Screen name="MainTabs" component={DoctorTabs} />

            <Stack.Screen name="ChatEntry" component={ChatList} />

            <Stack.Screen
              name="EditDietDay"
              component={EditDietDay}
              options={{ headerShown: true, title: "Edit Day" }}
            />

            <Stack.Screen
              name="CreateDietChart"
              component={CreateDietChart}
              options={{ headerShown: true, title: "Diet Chart" }}
            />

            <Stack.Screen name="ChatScreen" component={ChatScreen} />
            <Stack.Screen name="PatientProfile" component={PatientProfile} />
            <Stack.Screen name="EditPatient" component={EditPatient} />
            <Stack.Screen name="AddPatient" component={AddPatient} />
            <Stack.Screen name="Collections" component={Collections} />
            <Stack.Screen name="Recipes" component={Recipes} />
            <Stack.Screen name="DietChart" component={DietChart} />
            <Stack.Screen name="AutoGenerateDiet" component={AutoGenerateDiet} />
            <Stack.Screen name="AddFoodScreen" component={AddFoodScreen} />
            <Stack.Screen name="ChangePassword" component={ChangePassword} />
            <Stack.Screen name="EditUserProfile" component={EditUserProfile} />
          </>
        )}

        {/* ⭐ GLOBAL BOTPRESS CHAT SCREEN - AVAILABLE TO ALL ROLES */}
        <Stack.Screen
          name="BotpressChat"
          component={BotpressChatScreen}
          options={{
            headerShown: true,
            title: "AyurBot AI",
            headerStyle: { backgroundColor: "#fff" },
            headerTintColor: colors.primary,
          }}
        />

      </Stack.Navigator>
    </NavigationContainer>
  );
}

export default function App() {
  return (
    <AuthProvider>
      <KeyboardProvider>
        <AppNavigator />
      </KeyboardProvider>
    </AuthProvider>
  );
}
