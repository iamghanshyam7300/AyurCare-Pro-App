const Stack = createStackNavigator();
const Tab = createBottomTabNavigator();

/* ---------------------- HEADER CHAT BUTTON ---------------------- */
function chatHeaderButton(navigation) {
  return (
    <TouchableOpacity
      onPress={() => navigation.navigate("ChatEntry")}
      style={{
        marginRight: 16,
        backgroundColor: colors.primary,
        width: 40,
        height: 40,
        borderRadius: 20,
        alignItems: "center",
        justifyContent: "center",
      }}
    >
      <Ionicons name="chatbubble-ellipses" size={20} color="#fff" />
    </TouchableOpacity>
  );
}

/* ---------------------- ADMIN TABS ---------------------- */
function AdminTabs() {
  return (
    <Tab.Navigator
      screenOptions={{ tabBarActiveTintColor: colors.primary, headerShown: false }}
    >
      <Tab.Screen
        name="AdminDashboard"
        component={AdminDashboard}
        options={{
          title: "Dashboard",
          tabBarIcon: ({ color, size }) => (
            <Ionicons name="grid-outline" color={color} size={size} />
          ),
        }}
      />

      <Tab.Screen
        name="DoctorAuthentication"
        component={DoctorAuthentication}
        options={{
          title: "Doctors",
          tabBarIcon: ({ color, size }) => (
            <Ionicons name="medkit-outline" color={color} size={size} />
          ),
        }}
      />
    </Tab.Navigator>
  );
}

